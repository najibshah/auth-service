//User Model
const mongoose = require("mongoose");
mongoose.model("User", {
  userID: {
    type: String,
    required: true,
  },
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});
